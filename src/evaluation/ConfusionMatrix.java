    public void getTextToClipboard(JTextField field) {
        try {
            double d = Double.parseDouble(field.getText());
            d = ProjectUtils.round(d, 3);
            ProjectUtils.toClipboard(ProjectUtils.formatDouble(d));
        } catch (Exception e) {
        }
    }

